def resolve_worst_tag(tag_codes: list[str]) -> str | None:
    """Deterministic worst_tag: lowest TAG_PRIORITY number wins.
    Returns None if tag_codes is empty."""
    if not tag_codes:
        return None
    return min(tag_codes, key=lambda t: TAG_PRIORITY.get(t, _TAG_PRIORITY_MAX))

def has_blocking_tag(tag_codes: list[str]) -> bool:
    """True if any tag in the list is REF or DEF."""
    return any(t in ("REF", "DEF") for t in tag_codes)

